<script setup lang="ts">
import { computed } from "vue";
import { RouterLink, RouterView, useRoute } from "vue-router";
import { apiFetch, setToken } from "./lib/api";
import ToastHost from "./components/ToastHost.vue";

const route = useRoute();
const isLoginPage = computed(() => route.path === "/login");
const isCourseGroup = computed(
  () =>
    route.path.startsWith("/courses") || route.path.startsWith("/categories"),
);
const isBlogGroup = computed(
  () =>
    route.path.startsWith("/blog-categories") ||
    route.path.startsWith("/blog-posts"),
);
const isServiceGroup = computed(
  () =>
    route.path.startsWith("/service-categories") ||
    route.path.startsWith("/service-posts"),
);

async function logout() {
  try {
    await apiFetch("/api/auth/logout", { method: "POST" });
  } catch {
    // ignore
  } finally {
    setToken(null);
    location.href = "/admin/login";
  }
}
</script>

